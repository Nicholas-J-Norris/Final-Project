package finalProjectNorris;

public class Student {
	//properties of the student class
	public String name;
	public int studentNumber;
	public String major;
	public int credits;
	
	public Student() {//non arg constructor
		
	}
	
	public Student(String name, int studentNumber, String major, int credits) {
		super();
		this.name = name;
		this.studentNumber = studentNumber;
		this.major = major;
		this.credits = credits;
	}
	
	
	
}
